public class EjemploClase6CaracteresCicloPara {
    public static void main(String[] args ){
        char[] caracter = new char[4];
        caracter[0] = 'H';
        caracter[1] = 'O';
        caracter[2] = 'L';
        caracter[3] = 'A';
        for(int i = 0; i < 4; i ++){
            System.out.println(caracter[i]);
        }
    }
}
