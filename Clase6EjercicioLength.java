//.length sirve para poner la cantidad de caracteres que tiene guardado el vector

public class Clase6EjercicioLength {
    public static void main(String[]args) {
       String[] palabras = {"Palabra1" , "Palabra2" , "Palabra3" , "Palabra4"};
       for(int i = 0; i < palabras.length; i ++){
        System.out.println(palabras[i]);
       }
    }
}
